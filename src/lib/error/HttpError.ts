class HttpError extends Error {
  statusCode: HttpStatus;

  constructor(message: string, statusCode: HttpStatus) {
    super(message);
    this.statusCode = statusCode;
    this.name = HttpStatusCode[statusCode];
  }
}

export { HttpError };

